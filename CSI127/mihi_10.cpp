#include <iostream>
using namespace std;

int main ()
{
  int i;   //The index variable for the loop

  //Loop will repeat 10 times:
  for (i = 0; i < 10; i++)
  {
    cout << "Mihi cura futuri\n";
  }
  return 0;
}
