import turtle

tadashi = turtle.Turtle()

a = int(input("Please enter a number: "))
b = int(input("Enter another number: "))
c = int(input("Enter another: "))
d = int(input("Enter another: "))
e = int(input("Enter another: "))


tadashi.forward(100)
tadashi.left(a)
tadashi.forward(100)
tadashi.left(b)
tadashi.forward(100)
tadashi.left(c)
tadashi.forward(100)
tadashi.left(d)
tadashi.forward(100)
tadashi.left(e)

    
